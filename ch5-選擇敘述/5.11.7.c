#include <stdio.h>
#include <stdlib.h>

int main () {
	int x = 100, y = 200;
	printf("x > y? %d\n", x > y);
	printf("x >= y? %d\n", x >= y);
	printf("x < y? %d\n", x < y);
	printf("x <= y? %d\n", x <= y);
	printf("x == y? %d\n", x == y);
	printf("x != y? %d\n", x != y);
	printf("x = y? %d\n", x = y);
	
	return 0;
}
